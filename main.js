// This is a simple starter JavaScript program

// Function to say hello
function greet(name) {
  console.log("Hello, " + name + "!");
}

// Function to add two numbers
function add(a, b) {
  return a + b;
}

// Call the functions
greet("World");
let result = add(5, 3);
console.log("5 + 3 = " + result);
